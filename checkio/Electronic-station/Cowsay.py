COW = r'''
        \   ^__^
         \  (oo)\_______
            (__)\       )\/\
                ||----w |
                ||     ||
'''
TT = r''' ________________
< Checkio rulezz >
 ----------------
        \   ^__^
         \  (oo)\_______
            (__)\       )\/\
                ||----w |
                ||     ||
'''
def cowsay(text):
    TTT = ""
    if len(text) < 40:
        l1 = " " + "_" * (len(text) + 2) + "\n"
        l2 = "< " + text + " >\n"
        l3 = " " + "-" * (len(text) + 2) + ""
        TTT = '{0}{1}{2}{3}'.format(l1, l2, l3, COW)
        return TTT

